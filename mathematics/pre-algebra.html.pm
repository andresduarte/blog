◊section{Least Common Multiple}

  ◊bullet-list{
    ◊right-note{
      A multiple of a number is the product of that number and any other number
    }
    Smallest positive number that is a multiple of all numbers under consideration.


    Number that has all the elements of the prime factorization of numbers under consideration.
    ◊left-note{e.g.}
    ◊right-note{Prime factorization}
    ◊$${
      18 = 2 \times 3 \times 3 \quad 12 = 2 \times 2 \times 3
    }
    ◊$${LCM(18,12) = 2 \times 2 \times 3 \times 3}
    ◊$${LCM(18,12) = 36}
  }


◊section{Greatest Common Factor}

  ◊right-note{Two numbers with a greatest common factor of ◊strong{one} are refered to as relatively prime.}
  Largest number that divides evenly into all numbers under consideration
  ◊left-note{e.g.}
  ◊$${GCF(12, 8) = 4}


