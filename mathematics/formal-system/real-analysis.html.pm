◊section{Density}
◊margin-note{Density does not imply a lack of holes.}
Between any two distinct rational numbers lies a third.

The infinity of the holes in the rational line vastly outweights the infinity of the rational numbers.

◊section{Intervals}
The open interval $(a,b)$ is the set:
◊$${
  \{x \in \mathbb{R} \mid a < x < b \}
}
The close interval $[a,b]$ is the set:
◊$${
  \{x \in \mathbb{R} \mid a \leq x \leq b \}
}

Half-open or half-closed intervals are also possible. You can use infinity to define an interval but infinity itself cannot be an element of the interval.
